          <b> ข้อ 4 แนวทางในการกำกับดูแลและบริหารจัดการข้อมูลส่วนบุคคล</b>
          <p> เพื่อยึดมั่นเป็นหลักการและใช้เป็นแนวทางในการกำกับดูแลและบริหารจัดการข้อมูลส่วนบุคคลที่อยู่ในความครอบครองหรือควบคุมดูแลของ รฟม.</p>
          <ol>
